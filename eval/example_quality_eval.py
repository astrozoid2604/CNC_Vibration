import utils.quality_metrics as data_qm
import numpy as np
import argparse 

def main(args):

    '''
    This demo showcases a situation where synthetic data is generated by adding noise to real data, which is a simple and naive approach. 
    Then, the metrics are computed to assess the quality of the synthetic data relative to the real data. If the synthetic data generation 
    method is effective, the Wasserstein distance and KS-Test statistic should be low, and the autocorrelations of real and synthetic data 
    should be similar.

    Output: The selected metrics will be be output for each feature column. For example, selected metrics for 

    NOTE: This is for multivariate time-series data
    '''

    # Generating sample data for the demo:
    if args.demo:
        real_data, synthetic_data = data_qm.generate_demo_data()
    else:
        if not args.real_data or not args.synthetic_data:
            raise ValueError("Please provide both --real_data and --synthetic_data when not in demo mode!")
        
        # Load the datasets from files
        real_data = np.load(args.real_data)
        synthetic_data = np.load(args.synthetic_data)

    # Using the evaluation function:
    # metrics = data_qm.evaluate_synthetic_data_univariate(real_data, synthetic_data) # univariate
    metrics = data_qm.evaluate_synthetic_data_multivariate(real_data, synthetic_data) # multi-variate

    # Printing out the results:
    data_qm.print_metrics(metrics)


if __name__ == "__main__":

    '''
    Example of parsing:

    1) Demo mode    
    python example_quality_eval.py --tests all --demo

    2) Single test:
    python example_quality_eval.py --real_data /home/user/demo/data/real --synthetic_data /home/user/demo/data/synthetic --tests single --single_test wasserstein

    3) Multiple tests:
    python example_quality_eval.py --real_data /home/user/demo/data/real --synthetic_data /home/user/demo/data/synthetic --tests multiple --multiple_tests "wasserstein,ks"

    4) ALL tests:
    python example_quality_eval.py --real_data /home/user/demo/data/real --synthetic_data /home/user/demo/data/synthetic --tests all

    
    '''
    parser = argparse.ArgumentParser(description='Evaluate synthetic data against real data using various metrics.')

    parser.add_argument('--real_data', type=str, default=None, help='Path to the real data file.')
    parser.add_argument('--synthetic_data', type=str, default=None, help='Path to the synthetic data file.')

    parser.add_argument('--tests', type=str, choices=['single', 'multiple', 'all'], required=True, help='Whether to run a single test, multiple tests, or all tests.')
    parser.add_argument('--single_test', type=str, choices=['wasserstein', 'ks', 'spearman', 'mmd', 'cramer'], help='Which single test to run if --tests is set to single.')
    parser.add_argument('--multiple_tests', type=str, help='Which tests to run, comma separated, if --tests is set to multiple. Example: "wasserstein,ks".')

    # Add a new argument for demo mode
    parser.add_argument('--demo', action='store_true', help='Run in demo mode, generating sample real and synthetic data.')

    args = parser.parse_args()
    main(args)
    print('Application completed. Exiting now.')
